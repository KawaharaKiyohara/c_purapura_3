//0が返ってきたら衝突していない。
//1が返ってきたら上の壁と衝突している。
//2が返ってきたら下の壁と衝突している。
//3が返ってきたら左の壁と衝突している。
//4が返ってきたら右の壁と衝突している。
int TennisCourt::IsHitWall(Vector2 pos)
{
	if(pos.y <= position.y){
		//上の壁と衝突しているor壁を抜けている。
		return 1;
	}else if(pos.y >= position.y + TENNIS_COURT_H - 1){
		//下の壁と衝突しているor壁を抜けている。
		return 2;
	}else if(pos.x <= position.x){
		//左の壁と衝突しているor壁を抜けている。
		return 3;
	}else if(pos.x >= position.x + TENNIS_COURT_W - 1 ){
		//右の壁と衝突しているor壁を抜けている。
		return 4;
	}
	//衝突していない。
	return 0;
}
